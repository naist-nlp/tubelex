AUX_HEAD2BASE = {
    'なけれ': 'なければならない',
    'ざる': 'ざるをえない',
    'しか': 'しかない',
    'こと': 'ことになる'
    }


def aux2base(surface: str):
    head, tail = surface.split(' ', 1)
    base = AUX_HEAD2BASE.get(head)
    if base is not None:
        return base
    assert head == 'ね', head
    rear = tail.rsplit(' ', 1)[-1]
    return 'ねばならぬ' if rear == 'ぬ' else 'ねばならない'


